const fs = require('fs');
const path = require('path');

const out = path.join(__dirname, '..', 'src', 'public', 'js', 'dev-auth.js');
const content = `// Auto-generated by npm run dev\nwindow.AUTH_SERVER = window.AUTH_SERVER || 'http://localhost:3000';\n`;

fs.mkdirSync(path.dirname(out), { recursive: true });
fs.writeFileSync(out, content, 'utf8');
console.log('Wrote dev-auth to', out);
